#!/usr/bin/env node
import { globSync } from 'glob';
import { compileFromFile } from 'json-schema-to-typescript';
import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const backendRoot = resolve(__dirname, '../../backend');
const generatedDir = resolve(projectRoot, 'src/generated');

mkdirSync(generatedDir, { recursive: true });

const schemaPatterns = [
  `${backendRoot}/app/modules/*/events/*.schema.json`,
  `${backendRoot}/app/core/events/*.schema.json`,
];

const schemaFiles = [];
for (const pattern of schemaPatterns) {
  schemaFiles.push(...globSync(pattern));
}

if (schemaFiles.length === 0) {
  console.error('❌ No schema files found');
  process.exit(1);
}

console.log(`📦 Found ${schemaFiles.length} schema files`);

(async () => {
  try {
    const typeDeclarations = [];
    const eventMappings = [];
    let processed = 0;

    for (const schemaPath of schemaFiles) {
      try {
        const ts = await compileFromFile(schemaPath);

        // Extract event name: /events/product_created.schema.json → product_created
        const eventMatch = schemaPath.match(/[/\\]events[/\\]([^/\\]+)\.schema\.json$/);
        if (!eventMatch) {
          console.warn(`⚠️  Skipped: no match for ${schemaPath}`);
          continue;
        }

        const eventName = eventMatch[1];
        const interfaceName = eventName
          .split('_')
          .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
          .join('');

        typeDeclarations.push(ts);
        eventMappings.push(`  '${eventName}': {} as ${interfaceName},`);
        processed++;
      } catch (err) {
        console.error(`❌ Failed to compile ${schemaPath}:`, err.message);
      }
    }

    if (processed === 0) {
      console.error('❌ No schemas successfully compiled');
      process.exit(1);
    }

    const header = `/**
 * AUTO-GENERATED: Do not edit manually.
 * Generated by: frontend/scripts/generate-event-types.mjs
 * Generated at: ${new Date().toISOString()}
 */

`;

    const types = typeDeclarations.join('\n\n');
    const eventTypeMap = `\nexport const EventTypeMap = {\n${eventMappings.join('\n')}\n} as const;\n`;

    writeFileSync(resolve(generatedDir, 'events.ts'), header + types + eventTypeMap);
    console.log(`✅ Generated ${processed} event types → src/generated/events.ts`);
  } catch (err) {
    console.error('❌ Generation failed:', err);
    process.exit(1);
  }
})();
